public class Dona {
    private int Circular;

    public void setCircular(int Circular) {
        this.Circular = Circular;
    }

    public void setSabor(int Sabor) {
        this.Sabor = Sabor;
    }

    public void setColor(int Color) {
        this.Color = Color;
    }

    public void setGrosor(int Grosor) {
        this.Grosor = Grosor;
    }

    public void setTamaño(int tamaño) {
        this.tamaño = tamaño;
    }

    public int getCircular() {
        return Circular;
    }

    public int getSabor() {
        return Sabor;
    }

    public int getColor() {
        return Color;
    }

    public int getGrosor() {
        return Grosor;
    }

    public int getTamaño() {
        return tamaño;
    }
    
    private int Sabor;
    
    private int Color;
    
    private int Grosor;
    
    private int tamaño;
    
            
}
